"""Generate the 7-slide overview presentation.

Produces ``ppt/eegfmri_overview.pptx`` (gitignored) with two embedded
figures generated by matplotlib from our N=2 results (hardcoded since
results/ is gitignored). Upload the .pptx to Google Drive and open with
Google Slides; see ``ppt/IMPORT_TO_GOOGLE_SLIDES.md``.

Slide order:
  1. Preprocessing pipeline
  2. EO/EC fundamental results
  3. Other tasks (stimloc, rest alpha-BOLD GLM, SWM)
  4. Figure A — phenotype dissociation in alpha-BOLD GLM z[V1]
  5. Vigilance x WM (the headline)
  6. Figure B — load x lag x phenotype matrix (Bridwell test)
  7. Future directions

Usage:
    python scripts/generate_ppt.py
"""

from __future__ import annotations

import logging
from pathlib import Path

import matplotlib.pyplot as plt
import numpy as np
from pptx import Presentation
from pptx.util import Inches, Pt
from pptx.enum.shapes import MSO_SHAPE
from pptx.dml.color import RGBColor

logging.basicConfig(level=logging.INFO, format="%(levelname)s: %(message)s")
log = logging.getLogger(__name__)

OUT_DIR = Path(__file__).resolve().parent.parent / "ppt"
PPTX_PATH = OUT_DIR / "eegfmri_overview.pptx"
FIG_A_PATH = OUT_DIR / "fig_a_alpha_bold_zv1.png"
FIG_B_PATH = OUT_DIR / "fig_b_lag_matrix.png"

# ---------------------------------------------------------------------------
# Data (hardcoded from results/ — single source of truth in docs/RESULTS.md)
# ---------------------------------------------------------------------------

# Per-task alpha-BOLD GLM mean z[V1].
Z_V1 = {
    "sub-500":     {"rest": -1.093, "swm_run-1": -0.473, "swm_run-2": -0.885},
    "sub-1070302": {"rest": -0.129, "swm_run-1": -0.341, "swm_run-2": -0.063},
}

# Bridwell lag sweep — fixed 8-12 Hz band, trial-level alpha-BOLD r.
LAGS = [3, 5, 7, 9, 11]
LAG_MATRIX_ROWS = [
    ("sub-500 L1",     [-0.042, -0.029, -0.159, -0.084, +0.019]),
    ("sub-500 L5",     [-0.106, -0.245, -0.216, -0.117, -0.044]),
    ("sub-1070302 L1", [+0.132, +0.152, +0.050, -0.130, -0.230]),
    ("sub-1070302 L5", [+0.168, +0.153, +0.237, +0.222, +0.095]),
]


# ---------------------------------------------------------------------------
# Figures
# ---------------------------------------------------------------------------

def make_fig_a() -> None:
    """Bar chart: mean z[V1] across 3 tasks for the two subjects."""
    tasks = ["rest", "swm_run-1", "swm_run-2"]
    labels = tasks
    xs = np.arange(len(tasks))
    w = 0.35
    sub500 = [Z_V1["sub-500"][t] for t in tasks]
    sub107 = [Z_V1["sub-1070302"][t] for t in tasks]

    fig, ax = plt.subplots(figsize=(9, 5.4))
    b1 = ax.bar(xs - w/2, sub500, w, label="sub-500 (canonical)",
                color="#2ca02c", edgecolor="black")
    b2 = ax.bar(xs + w/2, sub107, w, label="sub-1070302 (vigilance-vulnerable)",
                color="#d62728", edgecolor="black")
    ax.axhline(0, color="black", lw=1)
    ax.axhline(-1.0, color="gray", lw=0.5, ls="--", alpha=0.4)
    ax.set_xticks(xs); ax.set_xticklabels(labels, fontsize=12)
    ax.set_ylabel("mean z[V1] from alpha-BOLD GLM", fontsize=12)
    ax.set_title("Alpha-BOLD coupling in V1 — canonical phenotype stays negative\n"
                 "across rest + working memory; vigilance-vulnerable hovers near zero",
                 fontsize=13)
    ax.legend(loc="lower right", fontsize=11)
    for rects in (b1, b2):
        for r in rects:
            h = r.get_height()
            ax.text(r.get_x() + r.get_width()/2, h - 0.06 if h < 0 else h + 0.04,
                    f"{h:+.2f}", ha="center", fontsize=10)
    ax.set_ylim(-1.4, 0.4)
    ax.grid(axis="y", alpha=0.25)
    plt.tight_layout()
    plt.savefig(FIG_A_PATH, dpi=200, bbox_inches="tight")
    plt.close(fig)
    log.info("wrote %s", FIG_A_PATH)


def make_fig_b() -> None:
    """Heatmap: 4 rows (subject x load) by 5 cols (HRF lag) of trial-level r."""
    data = np.array([r for _, r in LAG_MATRIX_ROWS])
    row_labels = [name for name, _ in LAG_MATRIX_ROWS]
    col_labels = [f"lag {l}" for l in LAGS]

    fig, ax = plt.subplots(figsize=(9, 5.4))
    vmax = float(np.abs(data).max()) * 1.05
    im = ax.imshow(data, cmap="RdBu_r", vmin=-vmax, vmax=vmax, aspect="auto")
    ax.set_xticks(np.arange(len(col_labels)))
    ax.set_xticklabels(col_labels, fontsize=12)
    ax.set_yticks(np.arange(len(row_labels)))
    ax.set_yticklabels(row_labels, fontsize=12)
    for i in range(data.shape[0]):
        for j in range(data.shape[1]):
            v = data[i, j]
            color = "white" if abs(v) > vmax * 0.55 else "black"
            ax.text(j, i, f"{v:+.2f}", ha="center", va="center",
                    color=color, fontsize=11)
    ax.set_title("Trial-level alpha-BOLD coupling × WM load × HRF lag\n"
                 "Red = positive (anti-canonical), Blue = negative (canonical)",
                 fontsize=13)
    ax.set_xlabel("HRF lag (TR)", fontsize=12)
    cb = plt.colorbar(im, ax=ax)
    cb.set_label("Pearson r (across trials in load)", fontsize=11)
    # Highlight cells: canonical strengthening (sub-500 L5 lag 5)
    # and the lag-shifted vs true sign-flip cells in sub-1070302.
    for (i, j) in [(1, 1), (2, 4), (3, 2)]:  # sub-500 L5 lag5, sub-107 L1 lag11, sub-107 L5 lag7
        rect = plt.Rectangle((j-0.5, i-0.5), 1, 1, fill=False,
                             edgecolor="black", lw=2.5)
        ax.add_patch(rect)
    plt.tight_layout()
    plt.savefig(FIG_B_PATH, dpi=200, bbox_inches="tight")
    plt.close(fig)
    log.info("wrote %s", FIG_B_PATH)


