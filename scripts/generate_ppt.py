def add_title_only_slide(prs: Presentation, title: str) -> "Slide":
    layout = prs.slide_layouts[5]  # Title only
    slide = prs.slides.add_slide(layout)
    slide.shapes.title.text = title
    for p in slide.shapes.title.text_frame.paragraphs:
        for run in p.runs:
            run.font.size = Pt(26)
            run.font.bold = True
    return slide


def add_bullets(slide, bullets: list[str], left: float = 0.5,
                top: float = 1.4, width: float = 12.0, height: float = 5.6,
                font_size: int = 16) -> None:
    tx = slide.shapes.add_textbox(Inches(left), Inches(top),
                                  Inches(width), Inches(height))
    tf = tx.text_frame
    tf.word_wrap = True
    for i, b in enumerate(bullets):
        p = tf.paragraphs[0] if i == 0 else tf.add_paragraph()
        p.text = b
        p.level = 0
        for run in p.runs:
            run.font.size = Pt(font_size)


def add_image_slide(prs: Presentation, title: str, image_path: Path,
                    caption: str) -> None:
    slide = add_title_only_slide(prs, title)
    pic = slide.shapes.add_picture(
        str(image_path), Inches(0.5), Inches(1.2),
        width=Inches(12.3), height=Inches(5.4),
    )
    cap = slide.shapes.add_textbox(Inches(0.5), Inches(6.7),
                                   Inches(12.3), Inches(0.5))
    p = cap.text_frame.paragraphs[0]
    p.text = caption
    for run in p.runs:
        run.font.size = Pt(13)
        run.font.italic = True


